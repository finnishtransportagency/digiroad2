  AND ASSET_TYPE_ID IN (30, 40, 50, 60, 70, 80, 90, 100, 120, 140, 160, 190, 210)
  AND CREATED_BY NOT IN ('dr1_conversion', 'vvh_generated', 'dr1conversion');
