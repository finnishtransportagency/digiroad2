package fi.liikennevirasto.digiroad2.service.pointasset

import fi.liikennevirasto.digiroad2._
import fi.liikennevirasto.digiroad2.asset.{AutoGeneratedUsername, BoundingRectangle, LinkGeomSource, PropertyValue, SimplePointAssetProperty}
import fi.liikennevirasto.digiroad2.client.RoadLinkInfo
import fi.liikennevirasto.digiroad2.dao.pointasset.{Obstacle, PostGISObstacleDao}
import fi.liikennevirasto.digiroad2.linearasset.{LinkId, RoadLink, RoadLinkLike}
import fi.liikennevirasto.digiroad2.service.RoadLinkService
import fi.liikennevirasto.digiroad2.user.User

case class IncomingObstacle(lon: Double, lat: Double, linkId: String, propertyData: Set[SimplePointAssetProperty], mValue: Option[Double] = None) extends IncomingPointAsset

case class IncomingObstacleAsset(linkId: String, mValue: Long, propertyData: Set[SimplePointAssetProperty])  extends IncomePointAsset


class ObstacleService(val roadLinkService: RoadLinkService) extends PointAssetOperations {

  type IncomingAsset = IncomingObstacle
  type PersistedAsset = Obstacle

  override def typeId: Int = 220
  val typePublicId = "esterakennelma"

  override def fetchPointAssets(queryFilter: String => String, roadLinks: Seq[RoadLinkLike]): Seq[Obstacle] = PostGISObstacleDao.fetchByFilter(queryFilter)
  override def fetchPointAssetsWithExpired(queryFilter: String => String, roadLinks: Seq[RoadLinkLike]): Seq[Obstacle] = PostGISObstacleDao.fetchByFilterWithExpired(queryFilter)
  override def fetchPointAssetsWithExpiredLimited(queryFilter: String => String, token: Option[String]): Seq[Obstacle] = PostGISObstacleDao.fetchByFilterWithExpiredLimited(queryFilter, token)

  override def setFloating(persistedAsset: Obstacle, floating: Boolean) : Obstacle = {
    persistedAsset.copy(floating = floating)
  }

  override def setAssetPosition(asset: IncomingObstacle, geometry: Seq[Point], mValue: Double): IncomingObstacle = {
    GeometryUtils.calculatePointFromLinearReference(geometry, mValue) match {
      case Some(point) =>
        asset.copy(lon = point.x, lat = point.y)
      case _ =>
        asset
    }
  }

  override def create(asset: IncomingObstacle, username: String, roadLink: RoadLink, newTransaction: Boolean): Long = {
    val mValue = GeometryUtils.calculateLinearReferenceFromPoint(Point(asset.lon, asset.lat), roadLink.geometry)
    if(newTransaction) {
      withDynTransaction {
        PostGISObstacleDao.create(setAssetPosition(asset, roadLink.geometry, mValue), mValue, username, roadLink.municipalityCode, createTimeStamp(), roadLink.linkSource)
      }
    } else {
        PostGISObstacleDao.create(setAssetPosition(asset, roadLink.geometry, mValue), mValue, username, roadLink.municipalityCode, createTimeStamp(), roadLink.linkSource)
    }
  }

  def createFromCoordinates(incomingObstacle: IncomingObstacle, roadLink: RoadLink, username: String, isFloating: Boolean): Long = {
    if(isFloating)
      createFloatingWithoutTransaction(incomingObstacle.copy(linkId = LinkId.Unknown.value), username, roadLink)
    else {
      checkDuplicates(incomingObstacle) match {
        case Some(existingAsset) =>
          updateWithoutTransaction(existingAsset.id, incomingObstacle, roadLink, username, Some(existingAsset.mValue), Some(existingAsset.timeStamp))
        case _ =>
          create(incomingObstacle, username, roadLink, false)
      }
    }
  }

  def getProperty(incomingObstacle: IncomingObstacle, property: String) : Option[PropertyValue] = {
    incomingObstacle.propertyData.find(p => p.publicId == property).get.values.map(_.asInstanceOf[PropertyValue]).headOption
  }

  def checkDuplicates(incomingObstacle: IncomingObstacle, withDynSession: Boolean = false): Option[Obstacle] = {
    val position = Point(incomingObstacle.lon, incomingObstacle.lat)
    val obstacleType = getProperty(incomingObstacle, typePublicId).get.propertyValue.toString
    val signsInRadius = PostGISObstacleDao.fetchByFilter(withBoundingBoxFilter(position, TwoMeters), withDynSession).filter(
      asset =>
        GeometryUtils.geometryLength(Seq(position, Point(asset.lon, asset.lat))) <= TwoMeters &&
          obstacleType == getProperty(asset.propertyData, typePublicId).get.propertyValue.toString
    )
    if(signsInRadius.nonEmpty) Some(getLatestModifiedAsset(signsInRadius)) else None
  }

  def getLatestModifiedAsset(signs: Seq[Obstacle]): Obstacle = {
    signs.maxBy(sign => sign.modifiedAt.getOrElse(sign.createdAt.get).getMillis)
  }

  def createFloatingWithoutTransaction(asset: IncomingObstacle, username: String, roadLink: RoadLink): Long = {
    val mValue = GeometryUtils.calculateLinearReferenceFromPoint(Point(asset.lon, asset.lat), roadLink.geometry)
    PostGISObstacleDao.create(setAssetPosition(asset, roadLink.geometry, mValue), mValue, username, roadLink.municipalityCode, createTimeStamp(), roadLink.linkSource)
  }

  override def update(id: Long, updatedAsset: IncomingObstacle, roadLink: RoadLink, username: String): Long = {
    withDynTransaction {
      updateWithoutTransaction(id, updatedAsset, roadLink, username, updatedAsset.mValue, None)
    }
  }

  def updateWithoutTransaction(id: Long, updatedAsset: IncomingObstacle, roadLink: RoadLink, username: String, mValue : Option[Double], timeStamp: Option[Long]): Long = {
    updateWithoutTransaction(id, updatedAsset, mValue, roadLink.geometry, roadLink.municipalityCode,
      roadLink.linkSource, username, timeStamp)
  }

  def updateWithoutTransaction(id: Long, updatedAsset: IncomingObstacle, mValue: Option[Double], linkGeom: Seq[Point],
                               linkMunicipality: Int, linkSource: LinkGeomSource, username: String, timeStamp: Option[Long], fromPointAssetUpdater: Boolean = false): Long = {
    val value = mValue.getOrElse(GeometryUtils.calculateLinearReferenceFromPoint(Point(updatedAsset.lon, updatedAsset.lat), linkGeom))
    getPersistedAssetsByIdsWithoutTransaction(Set(id)).headOption.getOrElse(throw new NoSuchElementException("Asset not found"))  match {
      case old if old.lat != updatedAsset.lat || old.lon != updatedAsset.lon =>
        expireWithoutTransaction(id)
        PostGISObstacleDao.create(setAssetPosition(updatedAsset, linkGeom, value), value, username, linkMunicipality,
          timeStamp.getOrElse(createTimeStamp()), linkSource, old.createdBy, old.createdAt, old.externalId, fromPointAssetUpdater, old.modifiedBy, old.modifiedAt)
      case _ =>
        PostGISObstacleDao.update(id, updatedAsset, value, username, linkMunicipality,
          Some(timeStamp.getOrElse(createTimeStamp())), linkSource, fromPointAssetUpdater)
    }
  }

  override def getByBoundingBox(user: User, bounds: BoundingRectangle) : Seq[PersistedAsset] = {
    val (roadLinks, _) = roadLinkService.getRoadLinksWithComplementaryAndChanges(bounds,asyncMode = false)
    super.getByBoundingBox(user, bounds, roadLinks)
  }

  override def createOperation(asset: PersistedAsset, adjustment: AssetUpdate): PersistedAsset = {
    new PersistedAsset(adjustment.assetId, adjustment.linkId, adjustment.lon, adjustment.lat, adjustment.mValue,
      adjustment.floating, asset.timeStamp, asset.municipalityCode, asset.propertyData, asset.createdBy,
      asset.createdAt, asset.modifiedBy, asset.modifiedAt, linkSource = asset.linkSource, externalId = asset.externalId)
  }

  override def adjustmentOperation(persistedAsset: PersistedAsset, adjustment: AssetUpdate, link: RoadLinkInfo): Long = {
    val updated = IncomingObstacle(adjustment.lon, adjustment.lat, adjustment.linkId, persistedAsset.propertyData.map(prop => SimplePointAssetProperty(prop.publicId, prop.values)).toSet)
    updateWithoutTransaction(adjustment.assetId, updated, Some(adjustment.mValue), link.geometry, link.municipality.getOrElse(throw new NoSuchElementException(s"${link.linkId} does not have municipality code")),
      persistedAsset.linkSource, AutoGeneratedUsername.generatedInUpdate, Some(adjustment.timeStamp), true)
  }

  def getFloatingObstacles(floating: Int, lastIdUpdate: Long, batchSize: Int): Seq[Obstacle] = {
    PostGISObstacleDao.selectFloatings(floating, lastIdUpdate, batchSize)
  }

  def updateFloatingAsset(obstacleUpdated: Obstacle): Unit = {
    PostGISObstacleDao.updateFloatingAsset(obstacleUpdated)
  }

  override def toIncomingAsset(asset: IncomePointAsset, link: RoadLink) : Option[IncomingObstacle] = {
     GeometryUtils.calculatePointFromLinearReference(link.geometry, asset.mValue).map {
       point =>  IncomingObstacle(point.x, point.y, link.linkId, asset.asInstanceOf[IncomingObstacle].propertyData)
     }
  }
}


